INT8U Get_Bit(INT8U *p,INT16U X_Size,INT16U X,INT16U Y)
{
  if((*(p+(Y/8)*(X_Size)+X))&(0x01<<(Y%8)))
    return 0x01;
  else
    return 0x00;

}

